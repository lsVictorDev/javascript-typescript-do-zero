// Number
const num1 = 10; // Number
const num2 = 20.14; // Number de ponto flutuante

// variável indefinida
let nomeAluno; // undefined - não aponta para local nenhuma na memória, apenas foi declarada.
nomeAluno = 23;
console.log(nomeAluno);

// Null
let sobrenomeAluno = null; // Nulo - não aponta para local nenhuma na memória.

// diferença de null para undefined
// Representa a ausência intencional de valor.
// É utilizado quando sabemos que a variável irá receber um valor posteriormente,
// mas, naquele momento, ela ainda não possui nenhum.
// Exemplo prático: um botão que altera a cor de fundo do site.
// A variável pode iniciar como null e, após a ação do usuário,
// receber um valor como "vermelho".

// undefined:
// Representa uma variável que foi declarada, mas ainda não recebeu nenhum valor.
// O próprio JavaScript atribui undefined automaticamente.
// Também ocorre quando tentamos acessar uma propriedade ou retorno de função
// que não existe ou não foi definido.

// Boolean
// O tipo de variável boolean é a que recebe dois tipos de valores, true ou false.

