// Formas de exibir um texto (string)
console.log('Meu nome é "Victor"'); // String entre aspas simples
console.log("Meu nome é 'Victor'"); // String entre aspas dupla
console.log(`Meu "nome" é 'Victor'`); // String entre Crase

// Formas de exibir valores numéricos (number)
console.log(35) // Number
console.log(15.22) // Número de ponto flutuante
